    private void getAllShedule() {
        try {
            dtm.setRowCount(0);
            ArrayList<SheduleDTO> allShedule=SheduleController.getAllShedule();
            for(SheduleDTO shed:allShedule){
                Object[] rowData={shed.getDoctId(),shed.getDoctName(),shed.getShedMon(),shed.getShedTue(),shed.getShedWed(),shed.getShedThu(),shed.getShedFri(),shed.getShedSat(),shed.getShedSun()};
                dtm.addRow(rowData);
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Shedule.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Shedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void autoSettime() {
       new Thread (new Runnable() {
            @Override
            public void run() {
                while(true){   
                    Date curDate = new Date();
                    SimpleDateFormat dateFormat =new SimpleDateFormat("HH:mm:ss:aa");
                    String currentTime =dateFormat.format(curDate);
                    lbltime.setText(currentTime);
                    try{
                        Thread.sleep(1000);
                    }catch(InterruptedException ex){
                        
                    }
                    
                }
            }
        }).start();
    }

