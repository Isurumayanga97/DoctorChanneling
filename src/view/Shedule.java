    private void btnshedsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnshedsaveActionPerformed

        try {
            String DoctId=txtShedDoctId.getText();
            String DoctName=txtShedDoctName.getText();
            String ShedMon=txtShedMonday.getText();
            String ShedTue=txtShedTuesday.getText();
            String ShedWed=txtShedWednessday.getText();
            String ShedThu=txtShedThursday.getText();
            String ShedFri=txtShedFriday.getText();
            String ShedSat=txtShedSaturday.getText();
            String ShedSun=txtShedSunday.getText();
            
            SheduleDTO shedModel = new SheduleDTO(DoctId, DoctName, ShedMon, ShedTue, ShedWed, ShedThu, ShedFri, ShedSat, ShedSun);
            boolean addShedule=SheduleController.addShedule(shedModel);
            if(addShedule){
                JOptionPane.showMessageDialog(this, "Added");
                clearAll();
                getAllShedule();
            }else{
                JOptionPane.showMessageDialog(this, "Failed");
            }
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnshedsaveActionPerformed

