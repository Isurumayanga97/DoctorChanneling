    private void cmbSelectDoctorItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbSelectDoctorItemStateChanged

        try {
            String DoctID=cmbSelectDoctor.getSelectedItem().toString();
            DoctorDTO searchDoctor=DoctorController.searchDoctor(DoctID);
            txtDoctorName.setText(searchDoctor.getDoctName());
            txtdoctDescription.setText(searchDoctor.getDoctSpecialType());
            
            ArrayList<DoctorDTO> allDoctors=DoctorController.getAllDoctor();
            String date=null;
//            String name=null;
            String value=null;
            cmbDoctShedule.removeAllItems();
            for(DoctorDTO d:allDoctors){
                if (d.getDoctId().equals(DoctID)) {
                    SheduleDAO dao = (SheduleDAO) DAOFactory.getInstance().getDAO(DAOFactory.DAOTypes.SHEDULE);   
                    Shedule search = dao.search(DoctID);
                    date=search.getShedMon();
                    value = "Monday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                    date=search.getShedTue();
                    value = "Tuesday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                    date=search.getShedWed();
                    value = "Wednesday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                     date=search.getShedThu();
                    value = "Thursday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                    date=search.getShedFri();
                    value = "Friday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                     date=search.getShedSat();
                    value = "Saturday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                     date=search.getShedSun();
                    value = "Sunday"+ " "+ date;
                    cmbDoctShedule.addItem(value);
                }else{
                    
//                JOptionPane.showMessageDialog("Error", JOptionPane.ERROR_MESSAGE);
                }
//                cmbSelectDoctor.addItem(d.getDoctId());
            }
            
              
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cmbSelectDoctorItemStateChanged

        
