    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        
        try {
            String appoinID=txtAppoinmentNumber.getText();
            String patientName=txtpateintName.getText();
            String patientNIC=txtPatientId.getText();
            String doctName=txtDoctorName.getText();
            String description=txtdoctDescription.getText();
            String date=txtAppoinmentDate.getText();
            String sesion=cmbDoctShedule.getSelectedItem().toString();
            int tel=Integer.parseInt(txtpatientTel.getText());
            
            AppoinmentDTO appoinModel = new AppoinmentDTO(appoinID,patientName, patientNIC, doctName, description, date, sesion, tel);
            boolean addAppoinment=AppoinmentController.addAppoinment(appoinModel);
            if(addAppoinment){
                JOptionPane.showMessageDialog(this, "Make Appoinmnet");
                clearAll();
                getAllAppoinmnet();
            }else{
                JOptionPane.showMessageDialog(this, "Failed");
            }
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        new Payment().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnDoneActionPerformed

