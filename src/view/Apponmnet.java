    private void getAllAppoinmnet() {
      try {
          dtm.setRowCount(0);
          ArrayList<AppoinmentDTO> allAppoinment=AppoinmentController.getAllAppoinment();
          for(AppoinmentDTO app:allAppoinment){
              Object[] rowData={app.getAppoinmentNum(),app.getPatientName(),app.getPatientId(),app.getDoctorName(),app.getDescription(),app.getAppoinmentdate(),app.getShedule(),app.getPatientTel()};
              dtm.addRow(rowData);
          }
          
      } catch (ClassNotFoundException ex) {
          Logger.getLogger(Apponmnet.class.getName()).log(Level.SEVERE, null, ex);
      } catch (SQLException ex) {
          Logger.getLogger(Apponmnet.class.getName()).log(Level.SEVERE, null, ex);
      }
    }

    
    
   
